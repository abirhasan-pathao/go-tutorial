// methods are functions with a special receiver argument.
// reciever is autocasted (pointer of value not dType) when method is called.
// methods can not be declared outside the package of their receiver type.
// pointer receivers can modify the value to which the receiver points.

package main

import (
	"fmt"
	"math"
)

type Vertex struct {
	X, Y float64
}

func (v Vertex) Abs() float64 {
	return math.Sqrt(v.X*v.X + v.Y*v.Y)
}

func (v *Vertex) Scale(f float64) {
	v.X = v.X * f
	v.Y = v.Y * f
}

func main() {
	v := Vertex{3, 4}
	v.Scale(10)
	fmt.Println(v.Abs())
}
